    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try{
            PreparedStatement ps = cn.prepareStatement("INSERT INTO empleados (nombreEmp, telefono, email, sexEmp, direccion, ComisionEmp, salEmp, cargoEmp,  Departamento) VALUES (?,?,?,?,?,?,?,?,?)");
            ps.setString(1, txtNombre.getText());
            ps.setString(2, txtTelefono.getText());
            ps.setString(3, txtEmail.getText());
            ps.setString(4, txtSexo.getText());
            ps.setString(5, txtDireccion.getText());
            ps.setString(6, txtSalario.getText());
            ps.setString(7, txtComisiones.getText());
            ps.setString(8, txtPuesto.getText());
            ps.setString(9, txtDepartamento.getText());
            
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null,"DATOS GUARDADOS CORRECTAMENTE");
            mostrardatos();
            limpiarCampos();

        } catch (SQLException e){
            System.out.println("Error al registrar empleados " + e);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

