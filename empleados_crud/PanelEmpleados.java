    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        if(JOptionPane.showConfirmDialog(rootPane,"ESTA SEGURO QUE QUIERE ELIMINAR", "SALIR", JOptionPane.YES_NO_CANCEL_OPTION) == 0);
        try{
            PreparedStatement ps = cn.prepareStatement("DELETE FROM empleados WHERE IdEmp ='" + txtId.getText() + "'");
            int indice = ps.executeUpdate();
            if (indice > 0){
                mostrardatos();
            }else {
                System.out.println("NO HA SELECCIONADO FILA");
            }
        } catch (SQLException e){
            System.out.println("ERROR AL ELIMINAR DATOS " + e);
        }
       limpiarCampos();
    }//GEN-LAST:event_jMenuItem1ActionPerformed

