    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try{
            PreparedStatement ps = cn.prepareStatement("Update empleados SET nombreEmp = '" + txtNombre.getText() + "', telefono = '" + txtTelefono.getText() + "', email = '" + txtEmail.getText() + "', sexEmp = '" + txtSexo.getText() + "', direccion = '" + txtDireccion.getText() + "', comisionEmp = '" + txtComisiones.getText() + "', salEmp = '" + txtSalario.getText() + "', cargoEmp = '" + txtPuesto.getText() + "', Departamento = '" + txtDepartamento.getText());
            int indice = ps.executeUpdate();
            
            if(indice > 0){
                JOptionPane.showMessageDialog(rootPane, "DATOS ACTUALIZADOS CORRECTAMENTE");
                mostrardatos();
                limpiarCampos();
            }else{
                JOptionPane.showMessageDialog(null, "NO SELECCIONO FILA");
            }
        }catch(SQLException e){
                   JOptionPane.showMessageDialog(null, "ERROR AL ACTUALIZAR DATOS" + e);
                    }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void tblDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDatosMouseClicked
        
        btnRegistrar.setEnabled(false);
        btnActualizar.setEnabled(true);
        
        int fila = this.tblDatos.getSelectedRow();
        this.txtId.setText(this.tblDatos.getValueAt(fila, 0).toString());
        this.txtNombre.setText(this.tblDatos.getValueAt(fila,1).toString());
        this.txtTelefono.setText(this.tblDatos.getValueAt(fila, 2).toString());
        this.txtEmail.setText(this.tblDatos.getValueAt(fila, 3).toString());
        this.txtSexo.setText(this.tblDatos.getValueAt(fila, 4).toString());
        this.txtDireccion.setText(this.tblDatos.getValueAt(fila, 5).toString());
        this.txtComisiones.setText(this.tblDatos.getValueAt(fila, 6).toString());
        this.txtSalario.setText(this.tblDatos.getValueAt(fila, 7).toString());
        this.txtPuesto.setText(this.tblDatos.getValueAt(fila, 8).toString());
        this.txtDepartamento.setText(this.tblDatos.getValueAt(fila, 9).toString());
    }//GEN-LAST:event_tblDatosMouseClicked

